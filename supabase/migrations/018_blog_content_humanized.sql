**Low-temp machines** use chemicals, usually chlorine-based, to sanitize at lower water temperatures. They use less energy and produce less steam, but you will incur ongoing chemical costs and need to manage their storage and dispensing. Some delicate items also last longer with lower temperatures.

There isn't one right answer for everyone. Your choice depends on your utilities, whether you prefer handling chemicals or using more energy, and what your local health authority requires.

